# Ordenando uma lista de forma permanente com o método sort()
cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort()

print("\n")
print("A lista é ordenada em ordem alfabética")
print(cars)
print("Ordenando a lista em ordem alfabética reversa:")
cars.sort(reverse=True)
print(cars)

print("A lista é ordenada em ordem crescente.")
nrs = [1, 4, 2, 44]
nrs.sort()
print(nrs)
print("Ordenando a lista em ordem decrescente:")
nrs.sort(reverse=True)
print(nrs)

